import UIKit

extension UIColor {
    
    @nonobjc class var dark: UIColor {
        return UIColor(red: 16.0 / 255.0, green: 11.0 / 255.0, blue: 32.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var white: UIColor {
        return UIColor(white: 1.0, alpha: 1.0)
    }
    
    @nonobjc class var veryLightPink: UIColor {
        return UIColor(white: 216.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var apricot: UIColor {
        return UIColor(red: 1.0, green: 150.0 / 255.0, blue: 113.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var melon: UIColor {
        return UIColor(red: 239.0 / 255.0, green: 130.0 / 255.0, blue: 98.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightdark: UIColor {
        return UIColor(red: 49.0 / 255.0, green: 43.0 / 255.0, blue: 67.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var maize: UIColor {
        return UIColor(red: 1.0, green: 221.0 / 255.0, blue: 79.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var black: UIColor {
        return UIColor(white: 0.0, alpha: 1.0)
    }
    
    @nonobjc class var palePurple: UIColor {
        return UIColor(red: 183.0 / 255.0, green: 182.0 / 255.0, blue: 188.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var brownishGrey: UIColor {
        return UIColor(white: 112.0 / 255.0, alpha: 1.0)
    }
}
